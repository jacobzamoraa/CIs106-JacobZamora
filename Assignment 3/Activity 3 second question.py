print("How old are you in years")
yearsOld = int(input())
monthsOld = yearsOld * 12
daysOld = yearsOld * 365
hoursOld = yearsOld * 365 * 24
secondsOld = yearsOld * 365 * 24 * 3600
print("You are" + str(monthsOld) + "months old")
print("You are" + str(daysOld) + "days old")
print("You are" + str(hoursOld) + "Hours old")
print("You are" + str(secondsOld) + "seconds old")
